# Try to connect to PostgreSQL with retry mechanism
import time
import socket

def test_postgresql_connection(max_retries=10, delay=2):
    """Test PostgreSQL connection with retry mechanism"""
    for attempt in range(max_retries):
        try:
            print(f"üîÑ Attempting PostgreSQL connection (attempt {attempt + 1}/{max_retries})...")
            
            # First test if host is reachable
            host = os.getenv('DB_HOST', 'localhost')  # Use localhost for local PostgreSQL
            port = int(os.getenv('DB_PORT', '5432'))
            
            # Try alternative hostnames/IPs for Discloud VLAN
            alternative_hosts = [
                host,  # Original hostname (postgresql)
                'postgresql.discloud.app',  # Alternative hostname
                'postgresql.discloud.com',  # Another alternative
                'postgresql',  # Direct hostname
                'localhost',  # Local connection
                '127.0.0.1',  # Local IP
            ]
            
            # Debug: Check if PostgreSQL process is running
            print("üîç Checking PostgreSQL process...")
            import subprocess
            try:
                result = subprocess.run(['ps', 'aux'], capture_output=True, text=True, timeout=5)
                if 'postgres' in result.stdout.lower():
                    print("‚úÖ PostgreSQL process found!")
                else:
                    print("‚ùå PostgreSQL process not found")
            except Exception as e:
                print(f"‚ùå Error checking processes: {e}")
            
            # Debug: Check network interfaces
            print("üîç Checking network interfaces...")
            try:
                result = subprocess.run(['ip', 'addr'], capture_output=True, text=True, timeout=5)
                print("Network interfaces:")
                for line in result.stdout.split('\n'):
                    if 'inet' in line and '127.0.0.1' not in line:
                        print(f"  {line.strip()}")
            except Exception as e:
                print(f"‚ùå Error checking network: {e}")
            
            # Debug: Try to resolve hostname
            print("üîç Trying to resolve hostname 'postgresql'...")
            try:
                import socket
                ip = socket.gethostbyname('postgresql')
                print(f"‚úÖ Hostname 'postgresql' resolves to: {ip}")
            except Exception as e:
                print(f"‚ùå Cannot resolve 'postgresql': {e}")
            
            # Debug: Check /etc/hosts
            print("üîç Checking /etc/hosts...")
            try:
                with open('/etc/hosts', 'r') as f:
                    hosts_content = f.read()
                    if 'postgresql' in hosts_content:
                        print("‚úÖ Found 'postgresql' in /etc/hosts:")
                        for line in hosts_content.split('\n'):
                            if 'postgresql' in line:
                                print(f"  {line.strip()}")
                    else:
                        print("‚ùå 'postgresql' not found in /etc/hosts")
            except Exception as e:
                print(f"‚ùå Error reading /etc/hosts: {e}")
            
            # Test each alternative host
            working_host = None
            for test_host in alternative_hosts:
                try:
                    print(f"üîç Testing host: {test_host}:{port}")
                    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
                    sock.settimeout(5)
                    result = sock.connect_ex((test_host, port))
                    sock.close()
                    
                    if result == 0:
                        print(f"‚úÖ Host {test_host}:{port} is reachable!")
                        working_host = test_host
                        break
                    else:
                        print(f"‚ùå Host {test_host}:{port} not reachable")
                except Exception as e:
                    print(f"‚ùå Error testing {test_host}:{port}: {e}")
            
            if not working_host:
                print(f"‚ùå No PostgreSQL hosts reachable (attempt {attempt + 1})")
                if attempt < max_retries - 1:
                    print(f"‚è≥ Waiting {delay} seconds before retry...")
                    time.sleep(delay)
                    continue
                else:
                    return False
            
            # Test PostgreSQL connection with working host
            import psycopg2
            conn = psycopg2.connect(
                host=working_host,
                port=port,
                user=os.getenv('DB_USER', 'guardiao'),
                password=os.getenv('DB_PASSWORD', 'PasswordGuardiaoAdmin2025!'),
                database=os.getenv('DB_NAME', 'guardiaodatabase'),
                connect_timeout=10
            )
            conn.close()
            print(f"‚úÖ PostgreSQL connection successful with host: {working_host}")
            
            # Update the working host for Django
            os.environ['DB_HOST'] = working_host
            return True
            
        except Exception as e:
            print(f"‚ùå PostgreSQL connection failed (attempt {attempt + 1}): {e}")
            if attempt < max_retries - 1:
                print(f"‚è≥ Waiting {delay} seconds before retry...")
                time.sleep(delay)
            else:
                return False
    
    return False

